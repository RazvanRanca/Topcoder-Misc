      public int getMaximum(int[] hints) {
          int max = Integer.MIN_VALUE;
         for(int i=0; i<hints.length; i++) {
             for(int j=hints[i]/2; j<=hints[i]; j++) {
                 int diff = 2*j - hints[i];
                 int prod = j* (hints[i]-j);
                 if(diff>=0) {
                     for(int k=0; k<hints.length; k++)
                         if(hints[k] == diff) {
                             if(prod > max)
                                 max = prod;
                             break;
                         }
                 }
             }
         }  
          return max;
      }	 
       
