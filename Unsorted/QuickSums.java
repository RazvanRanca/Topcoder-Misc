      public int minSums(String numbers, int sum) {
         int max = (int) Math.pow(2, numbers.length()-1) -1;
         int minPlus = 20;
         int curPlus = 0;
         for(int cur =0; cur <= max; cur ++){
             String bits = Integer.toBinaryString(cur);
             StringBuilder sb = new StringBuilder(bits);
             while(sb.length() < numbers.length()-1)
                 sb.insert(0, '0');
             bits = sb.toString();
             
             int start = 0;
             long curSum = 0;
             curPlus = 0;
             for(int i=0; i<bits.length(); i++) {
                 char c = bits.charAt(i);
                 if(c == '1') {
                     curSum += Long.parseLong(numbers.substring(start, i+1));
                     start = i+1;
                     curPlus++;
                 }
             }
             if(start != numbers.length())
                 curSum += Long.parseLong(numbers.substring(start, numbers.length()));
             if(curSum == sum && curPlus < minPlus)
                 minPlus = curPlus;


         }
         if(minPlus == 20)
             return -1;
         return minPlus;
      }	 
       
