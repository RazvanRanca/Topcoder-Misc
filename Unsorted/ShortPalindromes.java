      public String shortest(String base) {
        StringBuilder pal = new StringBuilder(base);
        String[][] din = new String[base.length()+1][base.length()+1];
        for(int i=0; i<=base.length(); i++)
            for(int j=0; j<=base.length(); j++) {
                din[i][j]="";
                if(j==1 && i<base.length())
                    din[i][j] = base.substring(i, i+1).toString();
            }
        for(int i=base.length()-1; i>=0;  i--)
            for(int j=2; j<=base.length() - i; j++) {
                String first = pal.substring(i, i+1).toString();
                String last = pal.substring(i+j-1, i+j).toString();
                if(first.equals(last))
                    din[i][j] = first + din[i+1][j-2] + last;
                else
                    din[i][j] = best(first + din[i+1][j-1] + first, last + din[i][j-1] + last);

            }
        return din[0][base.length()];
      }

      public String best (String a, String b) {
          if(a.length() < b.length())
              return a;
          if(a.length() > b.length())
              return b;
          if(a.compareTo(b) < 0)
              return a;
          return b;
      }
       
