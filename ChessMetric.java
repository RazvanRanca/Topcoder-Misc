      public long howMany(int size, int[] start, int[] end, int numMoves) {
         long[][] oldBoard = new long[size][size];
         long[][] newBoard = new long[size][size];
         int[] nRow = {-2,-2,-1,-1,-1,-1,-1, 0,0, 1, 1,1,1,1, 2,2};
         int[] nCol = {-1, 1,-2,-1, 0, 1, 2,-1,1,-2,-1,0,1,2,-1,1};
         for(int i=0; i<size; i++) {
             Arrays.fill(newBoard[i], 0);
             Arrays.fill(oldBoard[i], 0);
         }
         oldBoard[start[0]][start[1]] = 1;
         for(int move = 1; move <= numMoves; move++) {
             for(int row =0; row <size; row++)
                 for(int col=0; col<size; col++) {
                    if(oldBoard[row][col] != 0)
                        for(int n=0; n<nRow.length; n++)
                            if(row + nRow[n] >= 0 && row + nRow[n] < size && col + nCol[n]>=0 && col + nCol[n]<size)
                                newBoard[row + nRow[n]][col + nCol[n]] += oldBoard[row][col];
                 }
             for(int row =0; row <size; row++)
                 for(int col=0; col<size; col++) {
                     oldBoard[row][col] = newBoard[row][col];
                     newBoard[row][col]=0;
                 }
         }
         return oldBoard[end[0]][end[1]];
      }	 
       
