      public int getFortunate(int[] a, int[] b, int[] c) {
          HashSet<Integer> rez = new HashSet<Integer>();

          for(int i=0; i< a.length; i++)
              for(int j=0; j<b.length; j++)
                  for(int k=0; k<c.length; k++) {
                      int nr = a[i]+b[j]+c[k];
                      if(isFortunate(nr))
                          rez.add(nr);
                  }
          return rez.size();
      }

      public boolean isFortunate (int nr) {
          if(nr == 0)
              return false;
          while (nr > 0) {
              if(!(nr % 10 == 5 || nr%10 == 8) )
                  return false;
              nr/=10;
          }
          return true;
      }
       
