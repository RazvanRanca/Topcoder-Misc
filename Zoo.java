      public long theCount(int[] answers) {
         Arrays.sort(answers);
         if(answers[0] != 0)
             return 0;
         if(answers.length == 1)
             return 2;
         
         if(!(answers[1] ==0 || answers[1] == 1))
             return 0;
         int cc = 1;
         int limit  = 2;
         long sol = 1;
         boolean increment = false;
         for(int i=1; i<answers.length;) {
             
             if(increment) {
                 cc = 1;
                 i++;
                 if(i >= answers.length)
                     break;
                 if(! (answers[i] == answers[i-1] || answers[i] == answers[i-1] + 1) )
                    return 0;
             }
             
             increment = true;
             while(i<answers.length && answers[i] == answers[i-1]) {
                 cc++;
                 i++;
                 increment = false;
                 if(i >= answers.length)
                     break;
                 if(! (answers[i] == answers[i-1] || answers[i] == answers[i-1] + 1) )
                    return 0;
                 
             }
             
             
             if(cc == 1)
                 limit = 1;
             if(cc>limit)
                 return 0;
             if(! increment && cc == 2)
                 sol *=2;
                       
         }
         if(cc == 1)
             sol*=2;
         return sol;
      }	 
       
