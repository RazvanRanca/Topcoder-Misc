      public String isValid(int[] seq) {
         int cas = 0;
         if(seq.length < 4)
             return "NO";
         int off = seq[1]-seq[0];
         if(off<=0)
             return "NO";
         for(int i=1; i< seq.length- 1; i++) {
             int newOff = seq[i+1]-seq[i];
             if(off != newOff) {
                 switch(cas){
                     case 0: {
                         if(newOff >= 0)
                             return "NO";
                         else {
                             off = newOff;
                             cas = 1;
                         }
                     }
                     break;
                     case 1: {
                         if(newOff < 0)
                             return "NO";
                         else {
                             off = newOff;
                             if(newOff == 0)
                                cas = 2;
                             else
                                 cas=3;
                         }
                     }
                     break;
                     case 2: {
                         if(newOff <= 0)
                             return "NO";
                         else {
                             off = newOff;
                             cas = 3;
                         }
                     }
                     break;
                     case 3: {
                         if(newOff >= 0)
                             return "NO";
                         else {
                             off = newOff;
                             cas = 4;
                         }
                     }
                     break;
                     default:
                         return "NO";
                 }
             }
         }

         if(cas != 4)
             return "NO";
            return "YES";
      }

