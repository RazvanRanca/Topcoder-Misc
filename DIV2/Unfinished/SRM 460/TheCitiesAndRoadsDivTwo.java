      public int find(String[] map) {
        HashMap<Integer,Integer> trees = new HashMap<Integer, Integer>();
        ArrayList<Integer> treeInd = new ArrayList<Integer>();
        HashMap<Integer,Integer> treeSize = new HashMap<Integer, Integer>();

        for(int i=0; i<map.length; i++)
            trees.put(i, i);

        for(int i=0; i<map.length -1; i++)
            for(int j=i+1; j<map[0].length(); j++) {
                if(map[i].charAt(j) == 'Y'){
                    int key = trees.get(j);
                    for(int k=0; k<trees.size(); k++)
                        if(trees.get(k) == key)
                            trees.put(k, trees.get(i));
                }

            }
        int n = 0;
        for(int i=0; i<trees.size();i++) {
            if(!treeInd.contains(trees.get(i))) {
                treeInd.add(trees.get(i));
                treeSize.put(trees.get(i), 1);
                n++;
            }
            else {
                treeSize.put(trees.get(i), treeSize.get(trees.get(i)) + 1);

            }

        }


        int no = 1;
        if(n==1)
            no=0;
        else
            while(n>2) {
                no*=(int) n*(n-1)/2;
                n--;
            }

        int edges = no*2/treeInd.size();
        int tot = 0;
        for(int i=0; i<treeInd.size()-1;i++)
            for(int j=i+1; j<treeInd.size(); j++)
                tot += edges*treeSize.get(trees.get(i)) * treeSize.get(trees.get(j));

        if(tot == 0)
            return 1;
        return tot;
      }

