      public int bestInvitation(String[] first, String[] second) {
        HashMap<String,Integer> s = new HashMap<String,Integer>();

        int max = 1;
        for (int i=0; i<first.length; i++) {
            if(s.containsKey(first[i])) {
                s.put(first[i], s.get(first[i]) + 1);
                if(s.get(first[i]) > max)
                max = s.get(first[i]);
            }
            else
                s.put(first[i],1);

            if(s.containsKey(second[i])) {
                s.put(second[i], s.get(second[i]) + 1);
                if(s.get(second[i]) > max)
                    max = s.get(second[i]);
            }
            else
                s.put(second[i],1);
        }
            
            return max;

      }	 
       
