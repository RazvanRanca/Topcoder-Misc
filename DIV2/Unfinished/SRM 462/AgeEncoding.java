    public boolean checkSpecial(String candles, char cs) {
        int ind = 0;
        while(ind < candles.length() - 1) {
            if(candles.charAt(ind) == '1')
                return false;
            ind ++;
        }
        if(candles.charAt(ind) == cs)
            return true;
        return false;
    }

    public boolean smaller(int age, double base, String candles) {
        double res = 0.0;
        double factor= 1.0;
        for(int i=0; i<candles.length(); i++) {
            if(candles.charAt(candles.length()-i-1) == '1')
                res += factor;
            factor *= base;
            if(res >= age)
                return false;
        }
        return true;
    }

        double minDiff = 0.0000000001;
      public double getRadix(int age, String candlesLine) {
        if(checkSpecial(candlesLine, '1')) {
            if(age == 1)
                return -2;
            else
                return -1;
        }
        if(checkSpecial(candlesLine, '0')) {
            if(age == 0)
                return -2;
            else
                return -1;
        }
        double low = 0.0;
        double high = 100;
        while(low < high && high - low > minDiff) {
            double med = low + (high-low)/2;
            if(smaller(age,med,candlesLine))
                low = med;
            else
                high = med;
        }
        if(!smaller(age,low,candlesLine))
            return -1;
        else
            return low;
      }	 
       
