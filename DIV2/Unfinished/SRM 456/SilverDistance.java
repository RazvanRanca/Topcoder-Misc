      public int minSteps(int sx, int sy, int gx, int gy) {
          int count = 0;
          int dx = Math.abs(gx-sx);
          int dy = Math.abs(gy-sy);
          count += Math.min(dx,dy);
          int loc = Math.max(dx,dy) - Math.min(dx,dy);
          boolean up = false;
          if(sy < gy && dx < dy)
              up = true;
          if(loc %2 == 0 || up)
              count += loc;
          else {
              count+=loc+1;

             if(sy>gy && dx < dy)
                 count++;
          }
          return count;

      }	 
       
