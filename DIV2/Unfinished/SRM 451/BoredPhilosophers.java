      public int[] simulate(String[] text, int N) {
          int[] sol = new int[N];
          Set s = new HashSet();

          for(int i=1;i<text.length;i++)
              text[0] = text[0].concat(text[i]);

          text = text[0].split(" ");
            for(int i=1; i<=N; i++){
                for(int j=0; j<=text.length - i; j++) {
                    String cur = text[j];
                    for(int k=j+1;k<j+i;k++){
                        cur = cur.concat(" ");
                        cur = cur.concat(text[k]);
                    }
                    s.add(cur);

                }
                sol[i-1]=s.size();
                s.clear();
            }
          return sol;
      }

