      public int findMinimumTime(int[] trapX, int[] trapY) {
          int res = Integer.MAX_VALUE;
      
           for(int i=0; i<trapX.length; i++) {
               int dist = trapX[i]-1 + trapY[i]-1;
               if(dist < res)
                   res = dist;
           }
      return res;
      }	 
       
