      public int maximumSubset(String[] inequalities) {
         int[] no = new int[2006];
         Arrays.fill(no, 0);
         for(String s:inequalities) {
             String[] cur = new String[10];
             cur = s.split(" ");
             int cNo = Integer.parseInt(cur[2]);
             if(cur[1].equals("="))
                 no[cNo*2+2]++;
             else if(cur[1].equals("<"))
                    for(int i=0; i<cNo*2+2; i++)
                        no[i]++;
             else if(cur[1].equals(">"))
                    for(int i=cNo*2+3; i<=2005; i++)
                        no[i]++;
             else if(cur[1].equals("<="))
                    for(int i=0; i<=cNo*2+2; i++)
                        no[i]++;
             else if(cur[1].equals(">="))
                    for(int i=cNo*2+2; i<=2005; i++)
                        no[i]++;
         }
         int max = -1;
         for(int i=0; i<=2005; i++)
             if(no[i] > max)
                 max = no[i];

         return max;
      }	 
       
