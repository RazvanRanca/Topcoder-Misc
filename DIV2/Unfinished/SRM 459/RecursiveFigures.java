      public double getArea(int sideLength, int K) {

          double r = sideLength/2.0;
          double s = 0;
          double s2 = Math.sqrt(2);
          double area = 0;
          for(int i=1; i<K;i++) {
              s = r*s2;
              area += Math.PI*r*r - s*s;
              r = s/2;
          }
          area += Math.PI*r*r;
          return area;
      }

