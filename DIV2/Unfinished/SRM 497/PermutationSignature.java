      public int[] reconstruct(String signature) {
        int[] sol = new int[signature.length() + 1];
        int ind = 0;
        int nr = 2;
        sol[0] = 1;
        for(int i=0; i<signature.length(); i++) {
            char c = signature.charAt(i);
            if(c == 'I')
                sol[ind+1] = nr;
            else {
                int cur = ind;
                while(cur >= 0 && signature.charAt(cur) == 'D') {
                    sol[cur+1] = sol[cur];
                    cur--;
                }
                sol[cur+1] = nr;
            }
            ind++;
            nr++;
        }

        return sol;
      }	 
       
