      public String winner(int N, int M, int K, int L) {
        int dif = M-L;

        if(Math.abs(dif) < K && (K%2 != dif%2)) {
            int left = (K-1+dif)/2;
            int right = left - dif;
            if(Math.abs(left) <= M-1 && Math.abs(right) <= N-M)
                return "Romeo";
        }
        boolean trap = true;
        int i;
        if(K%2 == 1)
            return "Draw";
        else {
            i = 0;
        while(i<K) {

            if(M+i < N) {
                int cDif = M+i-L;
                 int left = (K-1+cDif)/2;
                 int right = left - cDif;
                if(!(Math.abs(cDif) < K && (K%2 != cDif%2)) && (Math.abs(left) <= M+i-1 && Math.abs(right) <= N-M-i) ) {
                   
                     {
                        trap = false;
                        break;
                    }

                }
            }

            if(M-i > 0) {
                int cDif = M-i-L;
                 int left = (K-1+cDif)/2;
                 int right = left - cDif;
                if(!(Math.abs(cDif) < K && (K%2 != cDif%2)) && (Math.abs(left) <= M-i-1 && Math.abs(right) <= N-M+i) ) {

                     {
                        trap = false;
                        break;
                    }

                
                }
            }
            i+=2;
        }
        }
        if(trap)
            return "Strangelet";
        else
            return "Draw";
      }	 
       
