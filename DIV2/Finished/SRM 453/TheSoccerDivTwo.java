      public int find(int[] points) {
            int x = points[0];
            int better = 0;
            int wins = 1;
            for(int i=1; i<points.length; i++) {
                if(points[i]<=x)
                    wins++;
                if(points[i]>x+3) {
                    wins++;
                    better++;
                }
            }

             for(int i=1; i<points.length; i++) {
                 if(points[i] == x+3) {
                     if(wins > 0)
                         wins--;
                     else {
                         wins++;
                         better++;
                     }
                 }
             }
            int draw = 0;
            for(int i=1; i<points.length; i++) {
                if(points[i] == x+1 || points[i] == x+2) {
                    if(wins > 0)
                        wins--;
                    else {
                        draw++;
                    }
                }
            }

            if(draw%2==1)
                  better++;

            return 1+better;
      }	 
       
