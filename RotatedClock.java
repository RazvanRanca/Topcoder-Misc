      public String getEarliest(int hourHand, int minuteHand) {
         for(int i=1; i<=15; i++) {
             if(isValid (hourHand, minuteHand)) {
                 int hour = hourHand / 30;
                 int minute = minuteHand / 6;
                 String hh = "" + hour;
                 if(hour < 10)
                     hh = "0" + hour;
                 if(hour == 0)
                     hh = "00";
                 String mm = "" + minute;
                 if(minute < 10)
                     mm = "0" + minute;
                 if(minute == 0)
                     mm = "00";
                 return (hh + ":" + mm);
                     
             }
             hourHand = (hourHand + 30)%360;
             minuteHand = (minuteHand + 30)%360;
                 
         }
         return "";
      }

      public boolean isValid (int hour, int min) {
          return ((hour*12)%360) == min;
      }
       
